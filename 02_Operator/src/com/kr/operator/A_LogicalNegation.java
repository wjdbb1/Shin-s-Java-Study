package com.kr.operator;

public class A_LogicalNegation {
	public void method1() {
		// 논리 부정 연산자 -> !

		boolean b1 = true;
		boolean b2 = false;

		System.out.println("b1 : " + b1);
		System.out.println("!b1 : " + !b1);

		System.out.println("b2 : " + b2);
		System.out.println("!b2 : " + !b2);
	}

}
